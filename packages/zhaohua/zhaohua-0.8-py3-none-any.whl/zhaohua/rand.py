#########################################################################################################

import random


def rand_numbers_list(n, i=0, j=9):
    # 初始化一个空列表，用于存储随机生成的数字
    random_nums = []
    # 循环n次，每次生成一个随机数字并添加到列表中
    for _ in range(n):
        # 从i到j中随机选择一个数字并添加到列表中
        random_nums.append(random.randint(i, j))

    # 返回包含n个随机数字的列表
    return random_nums


