# rand_numbers_list 生成n组数字组成的列表，每一组数字在i到j随机生成。i和j如果省略，则在0 - 9随机生成。

# 参数:
# n(int): 要生成的随机数的数量
# i(int): 随机数生成的下限（包括）
# j(int): 随机数生成的上限（包括）

# 返回:
# list: 包含n个随机数的列表

# 示例：生成10组浮点数，默认范围为的0到9
# numbers_list = rand_numbers_list(10,-100,100)
